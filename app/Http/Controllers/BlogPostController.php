     */
    public function store(Request $request)
    {
        $post = BlogPost::create([
            'title' => request('title'), 
            'image' => request('image'),
            'slug' => str_slug(request('title'), '-'),
            'summary' => request('summary'), 
            'body' => request('body'), 
            'featured' => 0
        ]);

        return redirect('/blog');

    }

